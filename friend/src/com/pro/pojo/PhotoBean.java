package com.pro.pojo;

public class PhotoBean {

	private int id;
	private String path;
	private int photoBoxId;
	private String creDateTime;
	private String name;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public int getPhotoBoxId() {
		return photoBoxId;
	}

	public void setPhotoBoxId(int photoBoxId) {
		this.photoBoxId = photoBoxId;
	}

	public String getCreDateTime() {
		return creDateTime;
	}

	public void setCreDateTime(String creDateTime) {
		this.creDateTime = creDateTime;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
}
