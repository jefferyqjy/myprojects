package bean;

public class Profit {
	
	public double profit;
	public String partID;
	public String partname;
	public String partstandard;
	public String partpackaging;
	public String dates;
	
	public double getProfit() {
		return profit;
	}
	public void setProfit(double profit) {
		this.profit = profit;
	}
	public String getPartID() {
		return partID;
	}
	public void setPartID(String partID) {
		this.partID = partID;
	}
	public String getPartname() {
		return partname;
	}
	public void setPartname(String partname) {
		this.partname = partname;
	}
	public String getPartstandard() {
		return partstandard;
	}
	public void setPartstandard(String partstandard) {
		this.partstandard = partstandard;
	}
	public String getPartpackaging() {
		return partpackaging;
	}
	public void setPartpackaging(String partpackaging) {
		this.partpackaging = partpackaging;
	}
	public String getDates() {
		return dates;
	}
	public void setDates(String dates) {
		this.dates = dates;
	}


	

}
